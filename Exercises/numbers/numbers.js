// 1. Make function which will take a array with numbers as a parameter and
//    calculate average number and round to hundredths.
function average(arr) {
  let result = arr.reduce(function(sum, current){
    return sum + current;
  });
  return (result / arr.length).toFixed(2);
}

// 2. Generate array to pass to function above with values, and length generated by Math.random.
function randomNumbersArray(length) {
  let arr = [];
  for (i = 0; i < length; i++){
    arr.push(randomBetweenZeroAndHundred());
  }
  return arr;
}

console.log('Average for array of 10 numbers: ' + average(randomNumbersArray(randomBetweenZeroAndHundred())));
