// 2. Implement function `extractNumbers` which will leave only numbers in passed string.
function extractNumbers(str) {
  let resultString = '';
  for (let char in str) {
    if (Number.isInteger(parseInt(str[char]))){
      console.log(str[char]);
      resultString += str[char];
    }
  }
  return resultString;
}

